class Aluno:
    def __init__(self, id_aluno=None, nome=None, cpf=None, telefone=None, id_instrutor=None, nome_instrutor=None):
        self.id = id_aluno
        self.nome = nome
        self.cpf = cpf
        self.telefone = telefone
        self.id_instrutor = id_instrutor
        self.nome_instrutor = nome_instrutor